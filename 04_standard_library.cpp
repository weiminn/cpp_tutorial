#include <iostream>
#include <cmath>

using namespace std;

int main(int argc, char const *argv[])
{
    cout << floor(1.2) << endl;
    cout << pow(2, 3.5) << endl;

    auto someNum = 5.3;
    cout << someNum << endl;

    int someBin = 0b11011;
    cout << someBin << endl;

    int someHex = 0xF3AF;
    cout << someHex << endl;
    return 0;
}
